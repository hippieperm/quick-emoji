  void _copyToClipboard(String emoji) {
    Clipboard.setData(ClipboardData(text: emoji));
    setState(() {
      _selectedEmoji = emoji;
      _copied = true;
    });

    // ë³µì‚¬ í‘œì‹œë¥¼ ì ì‹œ í›„ì— ì‚¬ë¼ì§€ê²Œ í•¨
    Future.delayed(const Duration(seconds: 1), () {
      if (mounted) {
        setState(() {
          _copied = false;
        });
      }
    });
  }

